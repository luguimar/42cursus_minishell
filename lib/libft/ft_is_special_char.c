#include "libft.h"

int	ft_is_special_char(char c)
{
	if (c == ' ' || c == '$' || c == '\"' || c == '\'' || c == '\\' || \
	c == '/' || c == '\0' || c == '!' || c == '#' || c == '%' \
	|| c == '&' || c == '(' || c == ')' || c == '=' || \
	c == '?' || c == '@' || c == '{' || c == '[' || c == ']' \
	|| c == '}' || c == '`' || c == '~' || c == '+' || c == '*' \
	|| c == '-' || c == '.' || c == ':' || c == ',' || c == ';' \
	|| c == '<' || c == '>' || c == '|')
		return (1);
	return (0);
}
